const I18N = {
  current: 'zh',
  init() {
    const saved = localStorage.getItem('smartdiff_locale');
    if (saved && I18N_MESSAGES[saved]) {
      this.current = saved;
    } else {
      this.current = (navigator.language || '').toLowerCase().startsWith('zh') ? 'zh' : 'en';
    }
    document.documentElement.lang = this.current === 'zh' ? 'zh-CN' : 'en';
  },
  t(key, ...args) {
    const dict = I18N_MESSAGES[this.current] || I18N_MESSAGES.zh;
    let msg = dict[key] !== undefined ? dict[key] : key;
    args.forEach((arg, i) => { msg = msg.replace('{' + i + '}', arg); });
    return msg;
  },
  setLocale(lang) {
    if (!I18N_MESSAGES[lang]) return;
    this.current = lang;
    localStorage.setItem('smartdiff_locale', lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
    this.applyDOMTexts();
    if (typeof reRenderAll === 'function') reRenderAll();
  },
  applyDOMTexts() {
    document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = this.t(el.dataset.i18n); });
    document.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = this.t(el.dataset.i18nTitle); });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = this.t(el.dataset.i18nPlaceholder); });
  }
};

const t = (k, ...a) => I18N.t(k, ...a);
